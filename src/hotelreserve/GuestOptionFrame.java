    /**
     * When logout button is clicked, ask for confirmation if the guest wants to
     * logout. If yes, proceed to login, if no, remain in this frame.
     */
    private void logoutLabelMouseReleased(MouseEvent evt) {//GEN-FIRST:event_logoutLabelMouseReleased
        //switch login frame
        int status=0;
        
        //show dialog box for confirmation (status = 0 - yes, 1 - no)
        status = JOptionPane.showConfirmDialog(jPanel2, "Do you really want to logout?", 
                "Logout", JOptionPane.YES_NO_OPTION);
        
        System.out.println("status: " + status);
        
        if(status == 0){
            LoginFrame login = null;
            login = new LoginFrame();
            login.displaylogin();
            this.setVisible(false);
            this.dispose();
        }else{
            // do nothing
        }
    }//GEN-LAST:event_logoutLabelMouseReleased
   
