    /*
        This method will get all the information from the registration 
        text fields for the creation a new guest. Account pin or ID of the 
        guest is also generated here which is a 6 - digit random number code.
    */
    public void signup(){ 
         boolean isIDvalid = true; 
        
         presentGuest = new Client[database.getGuestCounter()]; // set size of the Client array using guestCounter 
         presentGuest  = database.getAllGuest();    //presentGuest points to all guest 
         System.out.println("Loading file...");
         
         //Get the information from the textFields
         fname = fnameTextField.getText();
         mname = mnameTextField.getText();
         lname = lnameTextField.getText();
         bmonth = (String) birthMonthComboBox.getSelectedItem();
         bday   = (String) birthDayComboBox.getSelectedItem(); 
         byear  = (String) birthYearComboBox.getSelectedItem();
         mobileNum = mobileNumTextField.getText();
         email = emailTextField.getText();
         roomPref = "none";
         
         do{
         //generate account pin/ID number
            for(int i=0;i<database.getGuestCounter();i++){
               //generate random number of 6 - digit
               do{
                   accountID = (int) (Math.random() *1000000);
               }while(accountID<100000);
               
               //check if accountID is already present
               if(accountID == presentGuest[i].getAccountID()){
                  isIDvalid = false;
               }

            }
        }while(!isIDvalid);
           
        System.out.println(accountID + " " + fname + " " + mname + " " + 
                        lname + " " + bday + " " + mobileNum + " " + email+"\n");

        RegisterConfirmFrame confirm = new RegisterConfirmFrame();
            
        confirm.displayFrame();
        confirm.setNewGuest(accountID,fname,mname,lname,bmonth,bday,byear,mobileNum,email);

        //reset the registration text fields
        fnameTextField.setText("");
        mnameTextField.setText("");
        lnameTextField.setText("");
        birthMonthComboBox.setSelectedIndex(1);
        birthDayComboBox.setSelectedIndex(1);
        birthYearComboBox.setSelectedIndex(1);
        mobileNumTextField.setText("");
        emailTextField.setText("");
    }
    
    
    /*
        This method will check if all registration textfields are filled 
        with information. If the field is empty, change the label to RED,
        if the field is supplied with information, change the label
        color to BLACK.
    */
    public boolean checkRegistration(){
        boolean registrationValid = true;
        
        if(fnameTextField.getText().isEmpty()){
             registrationValid = false;
             fnameLabel.setForeground(Color.red);
        }else{
             fnameLabel.setForeground(Color.black);
        }
        
        if(lnameTextField.getText().isEmpty()){
             registrationValid = false;
             lnameLabel.setForeground(Color.red);
        }else{
             lnameLabel.setForeground(Color.black);
        }
        
        if(mnameTextField.getText().isEmpty()){
             registrationValid = false;
             mNameLabel.setForeground(Color.red);
        }else{
             mNameLabel.setForeground(Color.black);
        }
        
        if(emailTextField.getText().isEmpty()){
             registrationValid = false;
             emailLabel.setForeground(Color.red);
        }else{
             emailLabel.setForeground(Color.black);
        }
        
        if(mobileNumTextField.getText().isEmpty()){
             registrationValid = false;
             mobileNumLabel.setForeground(Color.red);
        }else{
             mobileNumLabel.setForeground(Color.black);
        }
        if(birthMonthComboBox.getSelectedItem().equals("Month")){
             registrationValid = false;
             BdayLabel.setForeground(Color.red);
        }else{
             BdayLabel.setForeground(Color.black);
        }
        
        if(birthDayComboBox.getSelectedItem().equals("Day")){
             registrationValid = false;
             BdayLabel.setForeground(Color.red);
        }else{
             BdayLabel.setForeground(Color.black);
        }
        
        if(birthYearComboBox.getSelectedItem().equals("Year")){
             registrationValid = false;
             BdayLabel.setForeground(Color.red);
        }else{
             BdayLabel.setForeground(Color.black);
        }
        
        System.out.println("registrationValid: " + registrationValid);
        
        return registrationValid;
    }
    

