    /**
     * This will add days and remove days based on month selected
     */
    private void birthMonthComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_birthMonthComboBoxActionPerformed

        switch((String)birthMonthComboBox.getSelectedItem()){
            
        //Add day
        case "January":   case "March": 
        case "May":       case "July":   
        case "September": case "November":
            
            if(birthDayComboBox.getItemCount() == 29){
                System.out.println("dayCount: " +birthDayComboBox.getItemCount());
                birthDayComboBox.addItem("29");
                
            }
            if(birthDayComboBox.getItemCount() == 30){
                System.out.println("dayCount: " +birthDayComboBox.getItemCount());
                birthDayComboBox.addItem("30");
                
            }
            if(birthDayComboBox.getItemCount() == 31){
                System.out.println("dayCount: " +birthDayComboBox.getItemCount());
                birthDayComboBox.addItem("31");
                
            }
            break;
        //Remove days;    
        case "February":
            if(birthDayComboBox.getItemCount() == 32){
                birthDayComboBox.removeItemAt(31);
            }
            if(birthDayComboBox.getItemCount() == 31){
                birthDayComboBox.removeItemAt(30);
            }

            break;
            
        //Remove day 31
        case "April":   case "June":
        case "August":  case "October":
        case "December":
            if(birthDayComboBox.getItemCount() == 32){
               birthDayComboBox.removeItem("31");
               System.out.println("dayCount: " +birthDayComboBox.getItemCount());
            }else{ 
                // do nothing
            }
            break;
        default:
            break;
        }
    }//GEN-LAST:event_birthMonthComboBoxActionPerformed

